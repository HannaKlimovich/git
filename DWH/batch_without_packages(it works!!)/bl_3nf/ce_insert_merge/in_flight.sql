  INSERT INTO bl_3nf.ce_flight
  SELECT bl_3nf.flight_seq.NEXTVAL,
    fl.f_YEAR ,
    f_MONTH ,
    DAY_OF_MONTH ,
    DAY_OF_WEEK ,
    FL_DATE ,
    UNIQUE_CARRIER ,
    AIRLINE_ID ,
    CARRIER ,
    TAIL_NUM ,
    FL_NUM ,
    ORIGIN_AIRPORT_ID ,
    ORIGIN_AIRPORT_SEQ_ID ,
    ORIGIN_CITY_MARKET_ID ,
    ORIGIN ,
    ORIGIN_CITY_NAME ,
    ORIGIN_STATE_ABR ,
    ORIGIN_STATE_NM ,
    ORIGIN_WAC ,
    DEST_AIRPORT_ID ,
    DEST_AIRPORT_SEQ_ID ,
    DEST_CITY_MARKET_ID ,
    DEST ,
    DEST_CITY_NAME ,
    DEST_STATE_ABR ,
    DEST_STATE_NM ,
    CRS_DEP_TIME ,
    DEP_TIME ,
    DEP_DELAY ,
    DEP_DELAY_NEW ,
    DEP_DEL15 ,
    CRS_ARR_TIME ,
    ARR_TIME ,
    ARR_DELAY ,
    ARR_DELAY_NEW ,
    ARR_DEL15 ,
    CRS_ELAPSED_TIME ,
    ACTUAL_ELAPSED_TIME ,
    AIR_TIME ,
    FLIGHTS ,
    DISTANCE ,
    DISTANCE_GROUP ,
    PLANE_ID ,
    PILOT_ID
  FROM
    (SELECT DISTINCT f_YEAR ,
      f_MONTH ,
      DAY_OF_MONTH ,
      DAY_OF_WEEK ,
      FL_DATE ,
      UNIQUE_CARRIER ,
      AIRLINE_ID ,
      CARRIER ,
      TAIL_NUM ,
      FL_NUM ,
      ORIGIN_AIRPORT_ID ,
      ORIGIN_AIRPORT_SEQ_ID ,
      ORIGIN_CITY_MARKET_ID ,
      ORIGIN ,
      ORIGIN_CITY_NAME ,
      ORIGIN_STATE_ABR ,
      ORIGIN_STATE_NM ,
      ORIGIN_WAC ,
      DEST_AIRPORT_ID ,
      DEST_AIRPORT_SEQ_ID ,
      DEST_CITY_MARKET_ID ,
      DEST ,
      DEST_CITY_NAME ,
      DEST_STATE_ABR ,
      DEST_STATE_NM ,
      CRS_DEP_TIME ,
      DEP_TIME ,
      DEP_DELAY ,
      DEP_DELAY_NEW ,
      DEP_DEL15 ,
      CRS_ARR_TIME ,
      ARR_TIME ,
      ARR_DELAY ,
      ARR_DELAY_NEW ,
      ARR_DEL15 ,
      CRS_ELAPSED_TIME ,
      ACTUAL_ELAPSED_TIME ,
      AIR_TIME ,
      FLIGHTS ,
      DISTANCE ,
      DISTANCE_GROUP ,
      PLANE_ID ,
      PILOT_ID
    FROM cl_flight
    ) fl
  WHERE F_YEAR      IS NOT NULL
  AND f_MONTH       IS NOT NULL
  AND DAY_OF_MONTH  IS NOT NULL
  AND TAIL_NUM      IS NOT NULL
  AND CARRIER       IS NOT NULL
  AND DEST_STATE_NM IS NOT NULL
  AND DEP_TIME      IS NOT NULL
  AND PLANE_ID      IS NOT NULL
  AND PILOT_ID      IS NOT NULL 
  ;
  COMMIT;